                <h3>Tips de Corte:</h3>
                <ul>
                    <li><strong>Corte en Juliana:</strong> Utiliza un cuchillo afilado para cortar en tiras finas. Es perfecto para vegetales como zanahorias y pimientos.</li>
                    <li><strong>Corte en Brunoise:</strong> Este es un corte en cubos muy pequeños, ideal para salsas o decoraciones.</li>
                    <li><strong>Corte en Mirepoix:</strong> Consiste en cortar cebolla, zanahoria y apio en trozos grandes para crear bases de sopas o guisos.</li>
                    <li><strong>Corte de Tomate:</strong> Usa un cuchillo de sierra para evitar aplastar el tomate. Así obtienes rebanadas perfectas.</li>
                </ul>
                <h3>Consejos de Seguridad:</h3>
                <ul>
